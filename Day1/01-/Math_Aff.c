#include <stdio.h>

int main()
{
    int a, b,res ;

    printf("Enter Number A : ");

    scanf("%d", &a);

    printf("Enter Number B : ");

    scanf("%d", &b);

    printf("A + B = %d\n", res = a + b);
    printf("A - B = %d\n", res = a - b);
    printf("A * B = %d\n", res = a * b);
    printf("A / B = %d\n", res = a / b);
}